def geometric_sum(n, r):
    # Базовый случай: если n равно 0, сумма равна 0
    if n == 0:
        return 0
    # Рекурсивный случай: текущий член (1) плюс сумма оставшихся членов
    else:
        return 1 + geometric_sum(n - 1, r) * r

# Пример использования
n = 5  # Количество членов
r = 2  # Знаменатель прогрессии
result = geometric_sum(n, r)
print(f"Сумма первых {n} членов геометрической прогрессии с r={r}: {result}")

